package sample;

public class Kopi {
    String Nama_kopi, Asal_kopi;
    int Stock;

    public String getNama_kopi() {
        return Nama_kopi;
    }

    public String getAsal_kopi() {
        return Asal_kopi;
    }

    public int getStock() {
        return Stock;
    }

    public void setNama_kopi(String nama_kopi) {
        Nama_kopi = nama_kopi;
    }

    public void setAsal_kopi(String asal_kopi) {
        Asal_kopi = asal_kopi;
    }

    public void setStock(int stock) {
        Stock = stock;
    }
}
